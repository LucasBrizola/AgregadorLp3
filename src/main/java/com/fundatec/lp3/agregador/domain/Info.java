package com.fundatec.lp3.agregador.domain;

public class Info {

	Tempo tempo;

	Zen zen;

	Secreto secreto;

	public Tempo getTempo() {
		return tempo;
	}

	public void setTempo(Tempo tempo) {
		this.tempo = tempo;
	}
	
	public Zen getZen() {
		return zen;
	}
	
	public void setZen(Zen zen) {
		this.zen = zen;
	}
	
	public Secreto getSecreto() {
		return secreto;
	}
	
	public void setSecreto(Secreto secreto) {
		this.secreto = secreto;
	}
}
